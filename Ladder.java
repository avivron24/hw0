public class Ladder {

    private int ladderStartingPoint;
    private int ladderEndingPoint;

    public Ladder(int ladderStartingPoint , int ladderEndingPoint){
        this.ladderStartingPoint = ladderStartingPoint;
        this.ladderEndingPoint = ladderEndingPoint;
    }

    public int getLadderStartingPoint() {
        return ladderStartingPoint;
    }

    public int getLadderEndingPoint() {
        return ladderEndingPoint;
    }

}
